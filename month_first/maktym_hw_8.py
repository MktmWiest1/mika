# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∑–∞–ø—Ä–∞—à–∏–≤–∞—Ç—å —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–º—è –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫.
# –ó–∞—Ç–µ–º —Ä–∞–±–æ—Ç–∞—Ç—å –≤ –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ–º —Ü–∏–∫–ª–µ –¥–æ –∏—Å—Ç–µ—á–µ–Ω–∏—è —É–∫–∞–∑–∞–Ω–Ω—ã—Ö –ø–æ–ø—ã—Ç–æ–∫.
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∑–∞–ø—Ä–∞—à–∏–≤–∞—Ç—å –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –¥–≤—É—Ö —Å–ª—É—á–∞–π–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 9.
# –ü–æ—Å–ª–µ –≤–≤–æ–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º –æ—Ç–≤–µ—Ç–∞, –≤—ã–≤–æ–¥–∏—Ç—å –Ω–∞ —ç–∫—Ä–∞–Ω –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç.
# –ö–∞–∂–¥—ã–π –≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç –∑–∞–ø–∏—Å—ã–≤–∞—Ç—å –≤ —Ñ–∞–π–ª results.txt
# –í —ç—Ç–æ—Ç –∂–µ —Ñ–∞–π–ª –¥–æ–ø–∏—Å–∞—Ç—å –∏—Ç–æ–≥–æ–≤—ã–π –æ—Ç—á—ë—Ç —Å —É–∫–∞–∑–∞–Ω–∏–µ–º –∏–º–µ–Ω–∏, –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø–æ–ø—ã—Ç–æ–∫ –∏ –∑–∞—Ç—Ä–∞—á–µ–Ω–Ω–æ–≥–æ –≤—Ä–µ–º–µ–Ω–∏.
import random
import time

name = input('–¢–∞–±–ª–∏—Ü–∞ —É–º–Ω–æ–∂–µ–Ω–∏—èüòä\n–í–≤–µ–¥–∏—Ç–µ –∏–º—è:')
attemps = int(input("–°–∫–æ–ª—å–∫–æ –ø–æ–ø—ã—Ç–æ–∫ —Ö–æ—á–µ—à—å?: "))
timesec = time.time()

print(name, attemps)

showattemps = attemps

while attemps != 0:
    attemps -= 1
    num1 = random.randint(1, 9)
    num2 = random.randint(1, 9)
    userInput = int(input(f'{num1} * {num2} = ? '))
    correct = num1 * num2

    if userInput == correct:
        print(f'–ü—Ä–∞–≤–∏–ª—å–Ω–æ!\n–û—Ç–≤–µ—Ç - {correct}')
        with open('../resultstabl.txt', 'a', encoding='UTF-8') as a:
            a.write(f"{num1} * {num2} ={correct} –ü—Ä–∞–≤–∏–ª—å–Ω–æ\n")

    else:
        print(f'–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ!\n–û—Ç–≤–µ—Ç - {correct}')
        with open('../resultstabl.txt', 'a', encoding='UTF-8') as a:
            a.write(f"{num1} * {num2} ={correct} –ù–µ—Ä–∞–≤–∏–ª—å–Ω–æ \n")
    print(f'–û—Å—Ç–∞–ª–æ—Å—å {attemps} –ø–æ–ø—ã—Ç–æ–∫!\n')

print(f'–ò–º—è –∏–≥—Ä–æ–∫–∞: {name}\n–ü–æ–ø—ã—Ç–æ–∫: {showattemps}\n–í—Ä–µ–º—è: - {round(time.time() - timesec)} —Å–µ–∫—É–Ω–¥\n ')
with open('../resultstabl.txt.txt', 'a', encoding='UTF-8') as a:
    a.write(f'–ò–º—è –∏–≥—Ä–æ–∫–∞: {name}\n–ü–æ–ø—ã—Ç–æ–∫:{showattemps}\n–í—Ä–µ–º—è: - {round(time.time() - timesec)} —Å–µ–∫—É–Ω–¥\n')
